# Solicitar datos al usuario
nombre = input("Ingresa tu nombre: ")
edad = input("Ingresa tu edad: ")
ciudad = input("Ingresa tu ciudad: ")

# Mensaje inicial
print("¡Hola,", nombre + "! Tienes", edad, "años y vives en", ciudad + ".")

# Inicio de la historia
print("Un día", nombre, "decidió salir a caminar por la ciudad de", ciudad + ".")
print("A sus", edad, "años, quería vivir una nueva aventura.")
print("Durante su paseo descubrió lugares interesantes y conoció nuevas personas.")
print("Al final del día,", nombre, "aprendió que cada aventura trae nuevas experiencias.")
# >>> Nuevas lineas añadidas <<<
print("Al día siguiente,", nombre, "decidió volver a explorar", ciudad + ".")
print("Mientras caminaba, pensó en todas las cosas que aún quería aprender.")
print("Con", edad, "años,", nombre, "sabía que todavía tenía muchas aventuras por vivir.")
# >>> Final de la historia <<<
print("Al llegar la noche,", nombre, "regresó a casa con el corazón lleno de gratitud.")
print("Sabía que", ciudad, "siempre tendría algo nuevo que ofrecerle.")
print("Y así, con", edad, "años,", nombre, "entendió que la magia está en vivir el presente.")
print("Fin.")
